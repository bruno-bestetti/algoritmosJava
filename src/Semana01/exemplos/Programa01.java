package exemplos;

public class Programa01 {

	public static void main(String[] args) {
		System.out.println("Ola, mundo");
		System.out.println("Linha 2");
		System.out.println("Linha 3");
		System.out.println("Linha 4");
		System.out.print("Linha 5");
		System.out.println("Ainda linha 5");
		
		// Declaração de uma variavel inteira
		int media;
		
		// atribuindo um valor a variavel media
		media = 5;
		System.out.println(media);
		media = 10;
		System.out.println(media);
		
		// Declarando e atribuindo um valor a uma nova variavel
		int idade = 29;
		
		// variavel do tipo double
		double preco = 2.99;
		System.out.println(preco);
	}

}
