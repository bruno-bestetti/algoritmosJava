package exemplos;

public class Programa05 {

	// Operadores aritméticos com variáveis
	public static void main(String[] args) {

		// Declarando dois números
		int num1 = 5;
		int num2 = 7;

		// Soma
		int soma = num1 + num2;
		System.out.println("A soma de " + num1 + " com " + num2 + " é iqual a: " + soma);

		// Subtração
		int diferenca = num2 - num1;
		System.out.println("Diferença: " + diferenca);

		// Divisão
		double divisao = (double) num2 / num1;
		System.out.println("Divisão: " + divisao);

	}

}
